// 获取按钮元素
const button1 = document.getElementById('colorButton1');

// 添加点击事件监听器
button1.addEventListener('click', function() {
    // 检查按钮是否已经有 'clicked' 类
    if (button1.classList.contains('clicked')) {
        button1.classList.remove('clicked');
        button1.classList.add('unclicked');
    } else {
        button1.classList.remove('unclicked');
        button1.classList.add('clicked');
    }
});

